          <h2 class="mb-0">Introduction</h2>
        </template>
        <p>Finding information in archival collections related to women’s suffrage in Iowa can be compared to a treasure
          hunt.</p>
        <p>Some collections, such as the Iowa Suffrage Memorial Commission and Woman’s Christian Temperance Union
          records,
          are obvious starting points because both organizations were known to be heavily involved in the suffrage
          movement.
        </p>
        <p>Others, such as the papers of Ellen Mowrer Miller (a nineteenth century pioneer woman), contain brief nuggets
          of
          suffrage information in diaries and letters. The <a href="http://digital.lib.uiowa.edu/suffrage"
            target="_blank">Women’s Suffrage in Iowa Digital Collection</a> strives to bring users a taste of the
          variety of
          information about women’s suffrage that has been preserved in the Iowa Women’s Archives and other institutions
          across the state.</p>
        <p>This guide gives a brief explanation of each collection used to create the digital collection and gives
          researchers an idea of what else is available in the archival collections.</p>
        <p>In the nineteenth and twentieth centuries, publishing standards like page numbers were not very standardized.
          Making these resources readable for a twenty-first century computer database and accessible for generations of
          future users can be a challenge. Therefore, digitization decisions made by IWA staff have been included in the
          collection descriptions. One hint for researchers is to make sure to look at the digital image for page
          numbers
          when preparing a citation. The page numbers in the navigation menu are for the computer and may not reflect
          the
          actual page number of the physical object.</p>
        <p>For further suggestions on a wealth of local archival and special collections resources as well as books and
          journal articles, visit our <router-link to="/resources">Resources page</router-link>.</p>

