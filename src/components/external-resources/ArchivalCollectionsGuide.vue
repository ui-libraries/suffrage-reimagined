        <p><a name="IWA"><strong></strong></a><strong><a href="http://www.lib.uiowa.edu/iwa/"
              target="_blank">To Archive</a></strong></p>
        
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0313"
            target="_blank">Margaret Atherton Bonney papers</a>. <em>1810-1992. 1.25 linear feet.</em></p>
        <p>Margaret Atherton Bonney was a historian and editor at the State Historical Society of Iowa in Iowa City. One
          of
          the people she researched was Mary Jane Whitely Coggeshall (1836-1911), known as “The Mother of Woman Suffrage
          in
          Iowa.” Included in Bonney’s research files are an <a href="http://digital.lib.uiowa.edu/u?/suffrage,1203"
            target="_blank">undated photograph</a> of Mary Jane Coggeshall and several brochures from Coggeshall’s
          posthumous induction into the Iowa Women’s Hall of Fame in 1990. Photocopies Bonney made of primary source
          documents from other repositories are not included in the digital collection, but are available to researchers
          at
          IWA. She did much of her research on Coggeshall using the <a
            href="http://sdrc.lib.uiowa.edu/suffrage/suffrage/CollectionRegister.pdf?func=direct&amp;doc_number=003490481&amp;local_base=iow06"
            target="_blank">Iowa Women’s Suffrage Collection</a> at the State Historical Society of Iowa in Des Moines.
        </p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0131"
            target="_blank">Lucille Ketchum Carter papers</a>. <em>1936-2003. 1 linear foot</em>.</p>
        <p>Lucille Ketchum Carter grew up in Missouri and graduated from nursing school at the State University of Iowa
          (now
          the University of Iowa). Later she actively campaigned for her husband, Iowa Congressman Steven V. Carter. In
          <a href="http://digital.lib.uiowa.edu/u?/suffrage,1110" target="_blank">undated writings</a>, she describes
          the
          first time her mother, Matilda Jane Ketchum, voted and how that experience influenced her own civic activities
          throughout her life.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0258"
            target="_blank">Cary Club of Marion, Iowa records</a>. <em>1883-1980. 1.5 linear feet.</em></p>
        <p>The Cary Club of Marion, Iowa was founded in 1878 to give women an outlet for intellectual improvement.
          Discussions and programs were held twice a month on a variety of topics including book reviews, historical
          subjects and current events such as suffrage. The <a href="http://digital.lib.uiowa.edu/u?/suffrage,1149"
            target="_blank">minutes</a> and <a href="http://digital.lib.uiowa.edu/u?/suffrage,1200"
            target="_blank">programs</a> included in the digital collection contain brief mentions of suffrage and women
          in
          politics in the 1900s, but also provide insight into how women’s clubs at all levels were discussing the
          issues of
          the day and organizing around common causes. The October 5, 1915 entry about the status of women includes an
          editorial comment from the secretary, which is a rare occurrence in club minutes from this time period.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0431"
            target="_blank">Jennifer Riggs Cosson papers</a>. <em>1890-1943. 10 linear inches.</em></p>
        <p>Jennifer Riggs Cosson attended Simpson College in Indianola, Iowa and graduated in 1899 with a bachelor’s
          degree
          in education. While she was there, she wrote a speech entitled <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,1786" target="_blank">“The American Girl of Today,”</a> in
          which
          she argued that women should not agitate for reform because it would come in its own time. In other speeches,
          she
          urged women should be determined in their pursuit of the ballot, but she never agreed with militant actions
          such
          as parades or demonstrations (<a href="http://digital.lib.uiowa.edu/u?/suffrage,1786" target="_blank">“Why
            Women
            Should Ask for the Ballot”</a>, undated). </p>
        <p>Jennifer Cosson became active in civic affairs, serving on the Des Moines Parks Board, the Simpson College
          Board,
          the Young Women’s Christian Association (YWCA) committee, P.E.O., and the Ladies Legislative League. </p>
        <p>In 1909, her husband, George Cosson, was elected to the Iowa Senate, and from 1911 to 1917 he held the office
          of
          Attorney General of the State of Iowa. In 1909, George Cosson is mentioned in the Woman’s Christian Temperance
          Union of Iowa <em>Proceedings</em> as supporting suffrage legislation. The bill he supported, however, quickly
          died. His papers are located in the <a href="http://www.lib.uiowa.edu/spec-coll/" target="_blank">University
            of
            Iowa Special Collections Department</a>.</p>

        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0365"
            target="_blank">Gwendolyn Wilson Fowler</a>. <em>1905-1996. 5.50 linear feet.</em></p>
        <p>Gwendolyn Fowler (1907 - 1997) was the first African American woman to obtain a pharmaceutical degree and to
          be
          officially registered as a pharmacist in the state of Iowa. She was a program analyst for the United State
          Foreign
          Service in Vietnam in the 1950s before returning to Des Moines and working as a pharmacist at Broadlawns
          Hospital.
        </p>
        <p>Fowler was a lifetime member of both the National Association for the Advancement of Colored People (NAACP)
          and
          the African Methodist Episcopal Church. She was also a member of the Des Moines Women’s Club and in 1987 was
          inducted into the Iowa Women’s Hall of Fame. The digital collection contains a <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,2837" target="_blank">sympathy note</a> for Fowler’s mother,
          Fannie Robinson Wilson, from the Des Moines Suffrage Club, but includes no more documentation specifically
          pertaining to suffrage. Various obituaries of Fowler’s friends and family members provide insight into the
          lives
          of African Americans in Iowa during this time, including membership in suffrage clubs such as the Mary Church
          Terrell Club.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0097"
            target="_blank">Mary Ankeny Hunter papers</a>. <em>1940. 1 item.</em></p>
        <p>Mary Ankeny Hunter was involved in the Polk County Woman Suffrage Society (later the Des Moines Political
          Equality Club), the League of Women Voters of Iowa, the Votes for Women League and the Polk County League of
          Women
          Voters. Beginning in 1922, she served as secretary of the Iowa Suffrage Memorial Commission, eventually
          holding
          the vice presidential and presidential offices. She was president when the memorial at the Iowa State Capitol
          was
          completed in 1936. This <a href="http://digital.lib.uiowa.edu/u?/suffrage,2720"
            target="_blank">autobiographical
            sketch</a> provides some information on major suffrage players in Iowa, but her
          correspondence can be found in the <a
            href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0398"
            target="_blank">Iowa Suffrage Memorial Commission records</a> in the Iowa Women's Archives.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0565"
            target="_blank">Iowa Federation of Women’s Clubs</a>. <em>1853-2006. 13 linear feet.</em></p>
        <p>Organized in 1893, by the 1920s, the Iowa Federation of Women’s Clubs (IFWC) had 810 clubs with over 40,000
          members. The IFWC’s goal was to connect clubwomen across the state through publications and conferences.
          Several
          Iowa clubs were in favor of suffrage and the <a href="http://digital.lib.uiowa.edu/u?/suffrage,1937"
            target="_blank">proceedings</a> of the IFWC reflect that. This collection also has information on the 1919
          bill
          that granted presidential suffrage to Iowa women. The club’s records in IWA also document the literary and
          civic
          pursuits of clubs across the state.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0398"
            target="_blank">Iowa Suffrage Memorial Commission records</a>. <em>1906-1941. 5 linear inches.</em></p>
        <p>Since the Iowa Suffrage Memorial Commission (ISMC) was an organization dedicated solely to recognizing the
          contributions Iowa women made to the suffrage campaign, this collection has been digitized in its entirety.
        </p>
        <p>The bulk of the collection consists of pamphlets and correspondence. The pamphlets are mainly pro-suffrage
          propaganda printed by the <a href="http://digital.lib.uiowa.edu/u?/suffrage,1315" target="_blank">Iowa Equal
            Suffrage Association (IESA)</a> and the <a href="http://digital.lib.uiowa.edu/u?/suffrage,1710"
            target="_blank">National American Woman Suffrage Association (NAWSA)</a>. Most of the <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,1398" target="_blank">correspondence</a> consists of letters
          between Carrie Chapman Catt, president of NAWSA and honorary president of the commission, and Mary Ankeny
          Hunter,
          president of the commission. Additional resources of the ISMC can be found in the <a
            href="http://sdrc.lib.uiowa.edu/suffrage/suffrage/CollectionRegister.pdf?func=direct&amp;doc_number=003490481&amp;local_base=iow06"
            target="_blank">Iowa Women’s Suffrage Collection</a> at the State Historical Society in Des Moines.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0020"
            target="_blank">League of Women Voters of Iowa</a>. <em>1920-1998. 77.5 linear feet.</em></p>
        <p>The League of Women Voters (LWV) of Iowa was founded in 1920 as the Iowa Equal Suffrage Association (IESA)
          disbanded. Women organized to educate other women about their new political responsibilities. The LWV
          histories
          include recollections from journalist Dorothy Ashby Pownall and activist Pauline Devitt. Reports of state
          organizers reveal how difficult it was to travel and host events in the 1920s. A League <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,246" target="_blank">scrapbook</a> contains photographs of
          many
          prominent IESA figures and essays about the history of women’s organizations in Iowa.</p>
        <p>The LWV record books are extensive; only the <a href="http://digital.lib.uiowa.edu/u?/suffrage,1975"
            target="_blank">early years</a> of the first book have been digitized.
          IWA and other state archival institutions have extensive records from local LWV chapters. See the
          <router-link to="/resources">Resource Guide</router-link> for further research suggestions.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0021"
            target="_blank">League of Women Voters of Johnson County</a>. <em>1920-2004. 14 linear feet.</em>
        </p>
        <p>The League of Women Voters of Johnson County was organized in 1920. Its <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,309" target="_blank">1922 <em>Bulletin</em></a> includes a
          good
          description of the League’s early aims. Since few bulletins from the early years of the League have survived,
          this
          is a rare glimpse into activities of the early 1920s.</p>
        <p>The <a href="http://digital.lib.uiowa.edu/u?/suffrage,300" target="_blank">Financial Accounts ledger from
            1920-1946 </a>includes notations interspersed between blank pages. The dozens of pages not digitized were
          completely blank. Researchers can see an example of those blank pages on Page 18 &amp; 19 in the digital
          collection. Any page with writing was digitized.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0643"
            target="_blank">Marion Federation of Women’s Clubs</a>. <em>1899-1957. 5 linear inches.</em></p>
        <p>The Marion Federation of Women’s Clubs was an umbrella organization that met monthly at the Marion Public
          Library, allowing individual women’s clubs to work together on community service projects. Suffrage was
          mentioned
          several times in the <a href="http://digital.lib.uiowa.edu/u?/suffrage,376" target="_blank">minutes</a> from
          1913-1917 and club members debated and eventually voted to found a Cedar County Suffrage League.
          Unfortunately,
          the records of this county league appear to have been lost.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0298"
            target="_blank">Ellen Mowrer Miller papers</a>. <em>1856-1994. 10 linear inches.</em></p>
        <p>Miller’s papers are an excellent example of how topical information can be “hidden” in archival collections.
          Ellen Mowrer Miller grew up in Boone County, Iowa after her family moved from Pennsylvania, and was not
          actively
          involved in the suffrage movement. However, she lived and wrote during the late nineteenth and early twentieth
          centuries when suffrage was a prominent topic in the news and she included at least one mention of suffrage in
          her
          <a href="http://digital.lib.uiowa.edu/u?/suffrage,348" target="_blank">diary</a>. A <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,325" target="_blank">letter</a> from her brother Milton
          Mowrer, a
          medical student in Philadelphia from 1866 to 1868, mentions a lecture by Henry Ward Beecher on Universal
          Suffrage.
          Milton Mowrer took his date to see Beecher speak for 75 cents. These brief references show that suffrage was
          being
          discussed by ordinary people across the country and not just by those actively involved in the movement. </p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0035"
            target="_blank">Louise Rosenfield Noun papers</a>. <em>1926-2002. 13 linear feet.</em></p>
        <p>Louise Rosenfield Noun was an Iowa historian, women’s rights activist and art collector. Her book <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,2308" target="_blank"><em> Strong-Minded Women: The Emergence
              of
              the Woman-Suffrage Movement in Iowa</em></a> (ISU Press, 1969) is considered the seminal work on women’s
          suffrage in Iowa. All subsequent research on suffrage in Iowa relied heavily on Noun. This work, and her book
          <a href="http://digital.lib.uiowa.edu/u?/suffrage,2524" target="_blank"><em>Leader and Pariah: Annie Savery
              and the
              Campaign for Women’s Rights in Iowa</em></a> (IWA, 2002) and her memoir, <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,2683" target="_blank"><em>Journey to Autonomy</em></a> (ISU
          Press, 1990), are included in their entireties in the digital collection. In her memoir, Noun describes her
          mother’s suffrage work, her own changing ideas about feminism, and surprises she encountered when researching
          the
          suffrage movement in Iowa. She also wrote numerous articles on suffrage and on figures such as Carrie Chapman
          Catt
          and Flora Dunlap. The drafts of these articles are available to researchers at the <a
            href="http://www.lib.uiowa.edu/iwa/" target="_blank">Iowa Women’s Archives</a>. (The research files for
          <em>Strong-Minded Women</em> are held by the <a
            href="http://www.lib.grinnell.edu/collections/specialcollections/Manuscripts/findingaids/Nounmss.html"
            target="_blank">Grinnell College Archives</a> in Grinnell, Iowa.)</p>
        <p>The Louise Noun papers also include photographs of Noun and her mother as well as a selection of suffrage
          research materials. Additional photos of Noun and a scrapbook can be found in the <a
            href="http://digital.lib.uiowa.edu/founders/index.php" target="_blank">Iowa Women’s Archives Founders
            Collection</a> in the Iowa Digital Library.</p>
        <p></p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0181"
            target="_blank">Elvira Gaston Platt papers</a>. <em>1853-1974. 1 linear inch.</em></p>
        <p>Elvira Gaston Platt, teacher and abolitionist, was born in New York in 1818 and moved west after her marriage
          in
          1841. In 1847 she and her husband moved to Fremont County, Iowa, turned to farming, and began assisting
          fugitive
          slaves on the underground railroad. During these years, Platt corresponded with her husband’s sisters,
          including
          Mrs. Mary A. Platt Darwin. Darwin was one of Iowa’s leading female lecturers in the 1860s and was present at
          the
          first women’s suffrage convention in Mt. Pleasant. A handful of the <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,1120" target="_blank">letters</a> address suffrage and
          mention
          Theodore Tilton’s publication <em>The Independent.</em> Other topics included in the Platt-Darwin letters
          include
          frontier life, travel difficulties, health concerns and family news.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0025"
            target="_blank">Dorothy Ashby Pownall papers</a>. <em>1918-1974. 2.5 linear inches.</em></p>
        <p>Journalist Dorothy Ashby was hired as a feature writer for the <em>Des Moines Capital</em> in 1917 and worked
          there until 1920. She was the only “girl reporter” to cover Camp Dodge in Des Moines during World War I, but
          she
          also wrote <a href="http://digital.lib.uiowa.edu/u?/suffrage,320" target="_blank">feature articles</a> on
          prominent Iowa women, including a handful on suffrage and women’s political activities. In 1955, Pownall gave
          a <a href="http://digital.lib.uiowa.edu/u?/suffrage,42" target="_blank">speech</a> to the League of Women
          Voters
          remembering her coverage of the last convention of the Iowa Equal Suffrage Association and how “dull” the
          suffragists could be. </p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0110"
            target="_blank">Professional Women’s League, Des Moines, records.</a> <em>1903-1998. 2.2 linear feet.</em>
        </p>
        <p> Rev. Mary A. Safford, who was active in the women’s suffrage movement, was elected the first president of
          the
          Professional Women’s League (PWL) in 1900.The purpose of the League was “to contribute to the social and
          intellectual life of its members, and to promote a spirit of fellowship among women in the professions.” As
          such,
          the collection includes <a href="http://digital.lib.uiowa.edu/u?/suffrage,2818" target="_blank">programs</a>
          and
          <a href="http://digital.lib.uiowa.edu/u?/suffrage,2801" target="_blank">minutes</a> referring to suffrage
          activities, including a <a href="http://digital.lib.uiowa.edu/u?/suffrage,2829" target="_blank">1913
            speech</a>
          arguing that women could not fully participate in professional activities without suffrage. There are also <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,2834" target="_blank">photos</a>, including one of Safford.
        </p>

        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0096"
            target="_blank">Proteus Club, Des Moines, records.</a> <em>1897-1996. 2.5 linear feet.</em></p>
        <p>The Proteus Club of Des Moines was organized in 1896 by seven women college graduates for their intellectual
          improvement. Members took turns preparing papers to read at club meetings. In 1913, the club presented a skit
          that
          parodied the British suffragettes, entitled “The Militant Husbandette.” The <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,1065" target="_blank">script</a> and <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,1047" target="_blank">photos</a> reveal attitudes toward
          “radical” suffragettes during this time period.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0424"
            target="_blank">Robinson-Lacy Family papers.</a> <em>1852-2001.10 linear inches.</em></p>
        <p>Laura Goddard Spaulding Robinson was present at an 1859 meeting of what would eventually be known as the
          Dubuque
          Ladies Literary Association (DLLA). Ten years later, the DLLA formed the Northeastern Iowa Suffrage
          Association-the first suffrage association in Iowa. <a href="http://digital.lib.uiowa.edu/u?/suffrage,2783"
            target="_blank">Photos</a>, <a href="http://digital.lib.uiowa.edu/u?/suffrage,2789"
            target="_blank">newspaper
            clippings</a>, and portions of a <a href="http://digital.lib.uiowa.edu/u?/suffrage,2333"
            target="_blank">DLLA
            scrapbook</a> are included in the <a href="http://digital.lib.uiowa.edu/suffrage" target="_blank">Women’s
            Suffrage in Iowa Digital Collection</a>. The Robinson-Lacy Family papers include family histories and an
          extensive collection of Robinson’s diaries. There are no specific mentions of suffrage activities in the
          diaries.
        </p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0099"
            target="_blank">Genevieve Buess Taylor papers.</a> <em>1916-1997. 1 linear inch.</em></p>
        <p>Genevieve Buess Taylor’s mother, Dorothy Mills (Buess) was born in 1897. Just before the polls opened for the
          1916 Iowa suffrage referendum, Mills wrote a <a href="http://digital.lib.uiowa.edu/u?/suffrage,1073"
            target="_blank">brief</a> arguing for equal suffrage in the United States. According to writing on the back
          of
          the brief, it was an assignment for an English class, probably when she was in high school. In 1919, she
          graduated
          from the Iowa State Teachers College in Cedar Falls, Iowa, acquiring a one-year college diploma.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0095"
            target="_blank">Welch and Angrick collection.</a> <em>1880-1995. 3.75 linear feet.</em></p>
        <p>This collection consists of materials purchased by Maryjo Welch and William Angrick at estate auctions and
          donated to the Iowa Women's Archives.</p>
        <p></p>
        <a href="http://digital.lib.uiowa.edu/u?/suffrage,2796" target="_blank">“Give Us the Ballot,”</a> sheet music
        written by Lilla C. Bliven for the Emmetsburg, Iowa Political Equality Club (1897) is one such donation. Bliven
        is
        listed in the <a href="http://www.ormsby.org/genie/John/Lilla_1857.html" target="_blank">1895 census of
          Emmetsburg</a> as a 37-year-old “keeping house” with two children and married to a real estate agent.
        <p></p>
        <p>Her son, Bruce Bliven wrote about his boyhood and family history in Emmetsburg, Iowa in the August 1968 issue
          of
          <em>The Palimpsest</em> (published by the State Historical Society of Iowa). His reminiscences include remarks
          about his mother as a poet and songwriter, although he does not mention “Give Us the Ballot” specifically.
          Bruce
          Bliven's papers at <a
            href="http://www.oac.cdlib.org/view?docId=tf5b69n7d4;query=;style=oac4;doc.view=entire_text"
            target="_blank">Stanford University Special Collections Department</a> contain a small amount of family
          correspondence.</p>
        <p>The Welch-Angrick physical collection also contains a scattering of national suffrage materials.</p>
        <p><a href="http://www.lib.uiowa.edu/findingaids/IWATitleDetail.aspx?CollectionIdentifier=IWA0751"
            target="_blank">Woman’s Christian Temperance Union records.</a> <em>1874-2006. 18.75 linear feet.</em></p>
        <p>The Woman’s Christian Temperance Union (WCTU) of Iowa was supportive of the suffrage movement from its fourth
          annual convention in Ottumwa in 1877. Publications of the WCTU, such as <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,1879" target="_blank"><em>The Iowa Signal</em></a>, often
          referred to a woman’s duty to clean up her town and protect it from the evils of intemperance. </p>
        <p>Several local temperance unions including <a href="http://digital.lib.uiowa.edu/u?/suffrage,945"
            target="_blank">Grinnell</a>, <a href="http://digital.lib.uiowa.edu/u?/suffrage,916" target="_blank">Story
            County</a>, <a href="http://digital.lib.uiowa.edu/u?/suffrage,897" target="_blank">Boone County</a>, and the
          <a href="http://digital.lib.uiowa.edu/u?/suffrage,922" target="_blank">Denison Total Abstinence Union</a> (not
          affiliated with WCTU) were also active on the suffrage front as evidenced in their minutes.</p>
        <p>Bound proceedings of Iowa conventions from <a href="http://digital.lib.uiowa.edu/u?/suffrage,449"
            target="_blank">1874</a> to <a href="http://digital.lib.uiowa.edu/u?/suffrage,894" target="_blank">1920</a>
          reveal the changing strategies used by suffragists and temperance workers. These books include detailed
          descriptions of activities, including financial information, and exhortations to support suffrage. These
          passages
          provide a rare glimpse behind the scenes of a powerful women’s organization. Personal opinions of individual
          women
          are printed in the club proceedings, which is unusual for records of that time. Since these proceedings are so
          extensive, only the sections pertinent to suffrage in Iowa have been digitized. Among other topics covered in
          convention proceedings (available in physical form at the Iowa Women’s Archives) are temperance, foreign
          workers,
          child welfare, and white slave trafficking. </p>
        <br>
        <p><strong>Printed Works Collection (IWA).</strong></p>
        <p>In addition to manuscript and photograph collections, the Iowa Women’s Archives also has a printed works
          collection consisting mainly of books by and about Iowa women. A handful of these are important documents of
          the
          women’s suffrage movement in Iowa and have been digitized for the collection.</p>
        <p>
        </p>
        <ul>
          <li><a href="http://digital.lib.uiowa.edu/u?/suffrage,2308" target="_blank"><em>Strong-Minded Women: the
                Emergence
                of the Woman-Suffrage Movement in Iowa</em> by Louise Rosenfield Noun (ISU Press, 1969).</a>
            <p></p>
            <p>This is <em>the</em> flagship work on women’s suffrage in Iowa. Noun spent countless hours tracking down
              primary sources scattered all over the country to document the nineteenth century movement in the Hawkeye
              state. Subsequent works on women’s suffrage in Iowa and the national movement rely heavily on this book.
              Her
              inability to locate relevant sources in Iowa inspired her to found the Iowa Women's Archives in 1992.
            </p>
          </li>
          <p></p>
          <p>
          </p>
          <li><a href="http://digital.lib.uiowa.edu/u?/suffrage,2524" target="_blank"><em>Leader and Pariah: Annie
                Savery
                and the Campaign for Women’s Rights in Iowa</em> by Louise Rosenfield Noun with Rachel E. Bohlman (Iowa
              Women's Archives, 2002).</a>
            <p></p>
            <p>This book highlights a forgotten leader of the women’s suffrage movement in Iowa: Annie Savery. Savery
              was a
              correspondent of Amelia Bloomer and one of the first women in Iowa to speak publicly about women’s rights.
              Later histories glossed over or edited out Savery’s contributions partly because she defended the right of
              free-love advocates to have their own opinions and spoke out against the conservative swing of Iowa
              suffrage
              societies in the early 1870s. The primary documents printed at the end of the book provide insight into
              the
              bitter internecine fights that cropped up in the movement.</p>
          </li>
          <p>
          </p>
          <li><a href="http://digital.lib.uiowa.edu/u?/suffrage,2716" target="_blank"><em>Women’s Suffrage in Iowa: 90
                Years
                After the “Winning Plan”</em> (The Blanden Art Museum, 2009).</a>
            <p></p>
            <p>This is a catalog for a suffrage exhibit curated by the Blanden Art Museum in 2009. Essays by Lindsay
              Shannon
              and Dianne Bystrom provide a concise history of the women’s suffrage movement in Iowa and of the life of
              Carrie Chapman Catt. In addition, there are many photographs and renderings of Iowa suffragists from the
              State
              Historical Society and the Webster County Historical Society.</p>
          </li>
          <p>
          </p>
          <li><a href="http://digital.lib.uiowa.edu/u?/suffrage,2326" target="_blank"><em>The Iowa Collection of
                Readings
                and Recitations Compiled by the Woman Suffrage Association, For the use of clubs and other Organizations
                in
                getting up Entertainments</em> (Des Moines, 1892).</a>
            <p></p>
          </li>
          <p>This is a national song and poem book for use at suffrage meetings and conventions. A selection has been
            digitized to give people an idea of how suffrage was being represented at this time, even though only one of
            the
            short pieces is local to Iowa.</p>
        </ul>

      </b-card>

