            Joseph A. Dugdale correspondence. 1 folder. (D879).</a></p>
        <p>Dating from 1866-1873, the bulk of this <a href="http://digital.lib.uiowa.edu/u?/suffrage,2954"
            target="_blank">correspondence</a> is from 1870, the year Dugdale spearheaded the first suffrage convention
          in
          Mount Pleasant. Many prominent Iowans were involved with the conference planning correspondence, including
          suffragist Amelia Bloomer. The tone of Bloomer’s letter suggests that she was a bit put out by the conference.
          She
          and fellow suffragist Annie Savery had hoped to hold the first suffrage conference in Des Moines, but that
          effort
          never got off the ground.</p>
        <p><a href="http://infohawk.uiowa.edu/F?func=direct&amp;doc_number=003507564&amp;local_base=iow06"
            target="_blank">
            Iowa Association of Colored Women’s Clubs. 1903-1972. 1 linear foot. (Ms193).</a></p>
        <p>The records in this collection include the organization’s constitution and by-laws; minutes, proceedings,
          programs and souvenirs of the annual meetings; and two scrapbooks, photographs and some records of the Central
          Association of Colored Women which represents clubs in Iowa, Minnesota, Missouri, Michigan, Wisconsin,
          Illinois,
          Indiana, and Kentucky. The records include materials from annual conventions in the following cities and
          towns:
          Davenport, Muscatine, Buxton, Ottumwa, Des Moines, Cedar Rapids, Sioux City, Marshalltown, Burlington, Iowa
          City,
          Mason City, Council Bluffs, Waterloo, and Sioux Falls, S.D.</p>
        <p>The minutes and programs from annual conventions reveal that there were several African American women’s
          clubs
          dedicated to suffrage (solely or in part). Few of the records go into detail on suffrage work, but it is clear
          that Iowa African Americans were involved in the fight. The minutes from <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,3201" target="_blank">1911</a> to <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,3352" target="_blank">1917</a> are also a treasure trove of
          information on education activism in the early twentieth century. Minutes of this organization can also be
          found
          in the <a href="http://digital.lib.uiowa.edu/aawiowa/index.php" target="_blank">African American Women in Iowa
            Digital Collection.</a></p>
        <p><a href="CollectionRegister.pdf?func=direct&amp;doc_number=003490481&amp;local_base=iow06"
            target="_blank">Iowa
            Women’s Suffrage Collection. 1868-1951. 9.5 cubic feet. (MS71).</a></p>
        <p>This collection includes records from several organizations such as the Iowa Women’s Suffrage Society, the
          Iowa
          Equal Suffrage Association, the Iowa Suffrage Memorial Commission, the Iowa League of Women Voters, and the
          Des
          Moines Political Equality Club. In addition, there are publications including a rare collection of Susan B.
          Anthony’s <em>The Revolution</em> (1868-1870), <em>The Woman’s Column</em> (1892), <em>The Woman Citizen</em>
          (1917-1918), and <em>The Woman’s Standard</em> (1886-1888). Several scrapbooks compiled by suffragists,
          newspaper
          clippings, correspondence, biographies, and materials belonging to Carrie Chapman Catt are also in the
          collection.
          Many <a href="http://digital.lib.uiowa.edu/u?/suffrage,3179" target="_blank">photographs</a> of suffragists
          have
          been removed from this collection and placed in the State Historical Society of Iowa’s photograph collection
          in
          Des Moines.</p>
        <p>Material relating to the Iowa Suffrage Memorial Commission, the Iowa League of Women Voters, the Des Moines
          Political Equality Club, and Carrie Chapmann Catt is included in the Women’s Suffrage in Iowa Digital
          Collection.
        </p>
        <p><em><a
              href="http://infohawk.uiowa.edu/F/?func=find-b&amp;find_code=SYS&amp;local_base=UIOWA&amp;request=003468243"
              target="_blank">The Woman’s Standard.</a> 1886-1911. Microfilm (originals in Des Moines).</em></p>
        <p><em>The Woman’s Standard</em> was the women’s suffrage publication in Iowa. Mary Jane Coggeshall and Martha
          C.
          Callanan compiled the first issue. <em>The Woman’s Standard</em> was published in various Iowa cities
          throughout
          its life, but the business offices remained in Des Moines. The <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,3489" target="_blank">1908 issues</a> have been digitized as
          a
          sample of the kinds of news stories suffragists reported on. </p>
        <p>Original issues are in the Iowa Women’s Suffrage Collection in Des Moines. Both State Historical Societies
          have
          the publication available on microfilm.</p>
        <hr>
        <br>
        <p><a name="SHSIC"></a><a href="http://www.iowahistory.org/libraries/index.html" target="_blank"><strong>STATE
              HISTORICAL SOCIETY OF IOWA, IOWA CITY</strong></a></p>
        
        <p><a
            href="http://infohawk.uiowa.edu/F/?func=find-b&amp;find_code=SYS&amp;local_base=UIOWA&amp;request=005816544"
            target="_blank">Charles City Political Equality Club.</a> <em>1891-1938. 2 volumes ; 21-22 cm. + 1 b &amp; w
            photograph.</em></p>
        <p>The Charles City Political Equality Club was established in November 1891 by Carrie Lane Chapman with
          fourteen
          charter members. The club’s constitution states that its purpose was to promote interest in “principles of
          impartial suffrage and ... secure such state and national legislation as shall be necessary to establish the
          same.” Members of the Political Equality Club were invited to read a suffrage paper to the WCTU. Other topics
          of
          discussion included women as poets and women as inventors. This collection includes <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,2922" target="_blank">two secretary’s books</a> containing
          the
          constitution, membership lists, and minutes of the Charles City Political Equality Club, with entries dated
          from
          the club’s first meeting on November 7, 1891 through March 10, 1897. There is also a black and white
          photograph
          taken in 1938 of Anna Mahara (the club’s first secretary) standing with Ivadelle Lane (Carrie Chapman Catt’s
          grandniece) in front of a marker at the girlhood home of Carrie Chapman Catt.</p>
        <p><a
            href="http://infohawk.uiowa.edu/F/?func=find-b&amp;find_code=SYS&amp;local_base=UIOWA&amp;request=003474851"
            target="_blank">Gilbert Haugen.</a> <em>1882-1940. 80 linear feet.</em></p>
        <p>Gilbert Nelson Haugen (1859 - 1933) was a seventeen-term Republican U.S. Representative from Iowa’s 4th
          congressional district--then located in northeastern Iowa--as well as a banker, farmer, real estate agent, and
          horse breeder. Born before the American Civil War, and first elected to Congress in the 19th century, Haugen
          served until his defeat in the 1932 Franklin D. Roosevelt landslide. </p>
        <p>This manuscript collection includes <a href="http://digital.lib.uiowa.edu/u?/suffrage,2972"
            target="_blank">correspondence</a> between Haugen and various Charles City area businessmen and their wives
          about the 1894 proposal to allow Iowa women to vote on bond issues. Partial suffrage was granted in 1894.
          Letters
          both for and against the measure are in the collection and a representative sample has been digitized. </p>
        <p>The collection also contains legislative, personal, and business papers, and speeches.</p>
        <p><u>Iowa Suffrage Association</u></p>
        <p>This collection includes <a href="http://digital.lib.uiowa.edu/u?/suffrage,3541" target="_blank">various
            documents</a> concerning women’s suffrage in Iowa, ranging from letters opposing suffrage to an Iowa Equal
          Suffrage Association registration card for war service and IESA stamps and fliers. </p>
        <p><a
            href="http://infohawk.uiowa.edu/F/?func=find-b&amp;find_code=SYS&amp;local_base=UIOWA&amp;request=003503565"
            target="_blank">Lawther Collection.</a> <em>1874- 1927. 3 linear feet.</em></p>
        <p>This collection is made of papers of suffrage activist Anna Bell Lawther and her father, William Lawther.
          Anna
          was born in Dubuque in 1872. She attended public school in Dubuque and received her bachelor’s degree from
          Bryn
          Mawr College. She embraced the issue of women’s suffrage, serving twice as the president of the Iowa State
          Equal
          Suffrage Association. She kept extensive clippings and <a href="http://digital.lib.uiowa.edu/u?/suffrage,3414"
            target="_blank">correspondence</a> on suffrage and anti-suffrage activities in Iowa, including
          correspondence
          with secretary of state William S. Allen regarding a mistake in filing which prevented a referendum from being
          sent to the Iowa people. Carrie Chapman Catt, president of the National American Woman Suffrage Association
          while
          Lawther served on IESA, was also a frequent correspondent. The collection also includes material on temperance
          and
          women’s work in World War I.</p>
        <p> Anna Lawther became a leading figure in local, state, and national Democratic Party politics, serving for
          six
          years on the Democratic National Central Committee. </p>
        <p><a
            href="http://infohawk.uiowa.edu/F/?func=find-b&amp;find_code=SYS&amp;local_base=UIOWA&amp;request=003504513"
            target="_blank">Jesse Macy.</a> <em>1862-1941. 6 ft. (15 boxes).</em></p>
        <p>Jesse Macy moved to Powesheik County, Iowa from Indiana in 1856. In 1859 he entered the academy of Iowa
          College
          at Grinnell, graduating from Iowa College (later known as Grinnell) in 1870. From 1885 until his retirement in
          1912 he taught political science and history at Grinnell. He was an advocate of women’s suffrage, the rights
          of
          blacks, Darwin’s theory of evolution, and peace. This collection includes correspondence, writings, diaries,
          photographs, and other material.</p>
        <p>One three-page <a href="http://digital.lib.uiowa.edu/u?/suffrage,3356" target="_blank">essay</a> in favor of
          woman’s suffrage has been included in the digital collection.</p>
        <p><u>Carrie Dean Pruyn</u></p>
        <p>Carrie Dean graduated from Tipton High School in 1895 and moved to Ida Grove after her marriage. In her <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,3515" target="_blank">reminiscences</a> she describes a
          suffrage
          auto tour and talks about suffrage work in rural Iowa. There are also several period <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,3524" target="_blank">photographs</a>.</p>
        <p><u>Suffrage Leaflets and Broadsides</u></p>
        <p>This loose folder is not part of its own collection, but items of interest for suffrage researchers include
          <a href="http://digital.lib.uiowa.edu/u?/suffrage,3505" target="_blank">leaflets and fliers</a> expressing
          opposition to poll taxes, and on the topics “Logic for the Business Man,” “True Democracy,” and “Women Do Want
          the
          Vote.”</p>
        <p><a
            href="http://infohawk.uiowa.edu/F/?func=find-b&amp;find_code=SYS&amp;local_base=UIOWA&amp;request=005816704"
            target="_blank">SUI Suffrage Club.</a> <em>1916-1917. 1 volume.</em></p>
        <p>The State University of Iowa (S.U.I.) Equal Suffrage Association was established in 1916 on the campus of the
          State University of Iowa (now the University of Iowa) to promote the cause of equal suffrage in the months
          prior
          to the June 1916 suffrage referendum. After the amendment was defeated, the organization resolved to “continue
          its
          existence until equal suffrage is granted to all citizens of the state of Iowa.” Included are <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,3394" target="_blank">minutes</a> of four meetings with a
          list of
          the names of 224 members. A number of significant University figures belonged to this club, including Ruth
          Gallaher and Bertha Shambaugh.</p>
        <p><em><a
              href="http://infohawk.uiowa.edu/F/?func=find-b&amp;find_code=SYS&amp;local_base=UIOWA&amp;request=003468243"
              target="_blank">The Woman’s Standard.</a> 1886-1911. Microfilm (originals in Des Moines).</em></p>
        <p>Iowa’s women’s suffrage publication. The <a href="http://digital.lib.uiowa.edu/u?/suffrage,3489"
            target="_blank">1908 issues</a> have been digitized in order to give examples of the types of articles
          published
          in this newspaper, which ran from 1886 to 1911.</p>

        <p><u><em>The Woman’s Journal.</em></u><em> May 1916</em></p>
        <p>This <a href="http://digital.lib.uiowa.edu/u?/suffrage,3533" target="_blank">May 1916</a> issue of the
          national
          publication, <em>The Woman’s Journal</em>, focused on the Iowa suffrage referendum. The writers seemed to
          think
          victory in Iowa was inevitable due to the support of so many prominent men and women.</p>
