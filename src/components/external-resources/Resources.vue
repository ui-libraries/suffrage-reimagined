              <li><strong><a href="http://digital.lib.uiowa.edu/tc/index.php" target="_blank">Traveling Culture: Circuit
                    Chautauqua in the Twentieth Century Digital Collection</a></strong>
                <p></p>


                <p>This digital collection presents promotional circulars (publicity brochures), musical and spoken word
                  recordings, and programs drawn from the Records of the Redpath Lyceum Bureau, the largest booking
                  agency for
                  circuit Chautauqua. Between 1904 and 1932, the enterprise brought together thousands of performers and
                  lecturers and millions of small town residents under large canvas tents. Take special note of
                  lecturers Mrs.
                  J. Redding Cole, Charles Zueblin, Belle Kearney, Jeannette Rankin, Charles Howard Plattenburg, Grace
                  Wilbur
                  Trout, S.A. Baldus, C.B. Rayhill, and Mecca Marie Varney for references to the suffrage movement, as
                  these
                  individuals each made a circuit stop in Iowa. Lecture titles include “Women Who Toil,” “Woman and the
                  Ballot,” “The Fallacy of Woman’s Suffrage,” and “The Citizen Mother.” </p>
              </li>
