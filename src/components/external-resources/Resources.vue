        <p>Local newspapers covered the suffrage movement from its inception until women won the right to vote. In the
          late nineteenth and early twentieth centuries, conversations about social and political issues were often
          carried out through the pages of the editorial sections. In addition, many newspapers either defended or
          vilified suffragists--and sometimes switched sides. Public libraries are often a good source for Iowa
          newspapers. They may have physical, microfilm or online copies.</p>


