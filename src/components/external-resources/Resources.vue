            Ohio. From 1890 to 1900, she was active in the temperance and woman suffrage movements. She organized
            temperance unions throughout southwest Missouri for the Missouri Woman’s Christian Temperance Union; was
            one of the early presidents of the Equal Suffrage Association of Missouri; and in the late 1890s worked in
            the suffrage movement in Mississippi, Louisiana, and Iowa. <p></p>
            <p>Harrison studied law at the University of Iowa and she attended Stanford University in 1894. Included
              in this collection are letters to her from suffrage leaders Susan B. Anthony, Rachel Foster Avery,
              Carrie Chapman Catt, and Mary Garrett Hay, and letters from Harrison to her father describing her
              experiences in and impressions of Mississippi, Louisiana, and Iowa.</p>
