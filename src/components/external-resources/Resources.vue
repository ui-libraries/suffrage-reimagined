        <p><strong>Library of Congress. By Popular Demand: “Votes for Women Suffrage” Pictures, 1850-1920.
            <a href="http://lcweb2.loc.gov/ammem/vfwhtml/vfwhome.html"
              target="_blank">http://lcweb2.loc.gov/ammem/vfwhtml/vfwhome.html</a></strong></p>
        <p>This is a small collection of 38 photos selected according to the following guidelines: “...images that
          have been popularly requested in the past, to include images of people and events that are mentioned
          prominently in standard works on women’s suffrage, and to select images for which no copyright restrictions
          are known and that are of reasonable image quality. Portraits of women active on behalf of woman suffrage
          and, more generally, women’s rights are included, as are images depicting the public, often symbolic, way in
          which suffragists campaigned for the right to vote. The set also features cartoons commenting on the
          movement, as well as a photograph depicting the activities of anti-suffragists, illustrating the manner and
          terms in which women's voting rights were debated.” There is one photo of Carrie Chapman Catt and photos of
          national leaders who campaigned in Iowa.</p>
        <p>Viewers may be interested in a national timeline of events from <a
            href="http://lcweb2.loc.gov/ammem/vfwhtml/vfwtl.html#top.html" target="_blank">1776 to 1920</a>.</p>

        <p><strong>Library of Congress. American Memory. American Women: A Gateway to Library of Congress Resources
            for the Study of Women’s History and Culture in the United States.<br>
            <a href="http://memory.loc.gov/ammem/awhhtml/index.html"
              target="_blank">http://memory.loc.gov/ammem/awhhtml/index.html</a></strong></p>
        <p>Unlike most American Memory presentations, “American Women” is not a collection of digital items. It is a
          gateway--a first stop for Library of Congress researchers working in the field of American women’s history.
        </p>
        <p>The site contains a slightly expanded and fully searchable version of the print publication <em>American
            Women: A Library of Congress Guide for the Study of Women’s History and Culture in the United States</em>.
          The guide has been redesigned for online use, with added illustrations and links to existing digitized
          material located throughout the Library of Congress Web site. These materials are supplemented by a small
          number of newly digitized items that provide a sample of the many relevant types of materials available in
          Library of Congress holdings. This gateway collection also provides information about African-American
          women’s involvement during the <a href="http://memory.loc.gov/ammem/awhhtml/awmss5/final_push.html"
            target="_blank">“Final Push”</a> for women’s suffrage.</p>

        <p><strong>Library of Congress. American Memory. Miller NAWSA Suffrage Scrapbooks, 1897-1911.
            <a href="http://memory.loc.gov/ammem/collections/suffrage/millerscrapbooks"
              target="_blank">http://memory.loc.gov/ammem/collections/suffrage/millerscrapbooks</a></strong></p>
        <p>Between 1897 and 1911 Elizabeth Smith Miller and her daughter, Anne Fitzhugh Miller, filled seven large
          scrapbooks with ephemera and memorabilia related to their work with women’s suffrage. The scrapbooks are a
          part of the National American Woman Suffrage Association Collection in the Rare Book and Special Collections
          Division. These scrapbooks document the activities of the Geneva Political Equality Club, which the Millers
          founded in 1897, as well as efforts at the state, national, and international levels to win the vote for
          women. They offer a unique look at the political and social atmosphere of the time and chronicle the efforts
          of two women who were major participants in the suffrage movement. This collection includes digitized
          materials (images, newspaper references, bulletins, and some correspondence) from the Carrie Chapman Catt
          Collection.</p>

        <p><strong>Library of Congress. American Memory. Votes for Women: Selections from the National American Woman
            Suffrage Association Collection, 1848-1921.<br>
            <a href="http://memory.loc.gov/ammem/naw/nawshome.html"
              target="_blank">http://memory.loc.gov/ammem/naw/nawshome.html</a></strong></p>
        <p>These online selections give users an idea of the wealth of information available in the complete National
          American Woman Suffrage Association (NAWSA) Collection available at the Library of Congress. The items were
          collected between 1890 and 1938 by members of NAWSA. The bulk of the collection is derived from the library
          of Carrie Chapman Catt.</p>
        <p> The collection includes an article entitled <a href="http://hdl.loc.gov/loc.rbc/rbnawsa.n2646"
            target="_blank">Legal Status of Women in Iowa</a> written by Jennie L. Wilson in 1894 and printed by the
          Des Moines Printing Company. </p>
        <p>In addition, Iowa suffrage researchers may be interested in <a
            href="http://hdl.loc.gov/loc.rbc/rbnawsa.n8216" target="_blank">Victoria Woodhull’s 1871 speech</a>
          defending free-love. The free-love question rocked Iowa suffragists in 1871 and 1872 and is blamed for the
          legislature failing to put an equal suffrage amendment to the people of Iowa at that time.</p>

        <p><strong>Library of Congress. American Memory. Women of Protest: Photographs from the Records of the
            National Woman’s Party.<br>
            <a href="http://memory.loc.gov/ammem/collections/suffrage/nwp/"
              target="_blank">http://memory.loc.gov/ammem/collections/suffrage/nwp/</a></strong></p>
        <p>The National Woman’s Party represented the militant wing of the suffrage movement. This collection of
          photographs is a representative selection illustrating the NWP’s tactics and techniques such as picketing,
          pageantry, parades, and demonstrations, which often led to arrests, imprisonment, and hunger strikes. The
          collection also highlights individual organization leaders and party members.</p>
        <p>Take note of two photographs highlighting Iowa women: </p>
        <p>The first is a portrait of <a href="http://hdl.loc.gov/loc.mss/mnwp.149003" target="_blank">Ilia
            Carpenter</a>, secretary of the Iowa Branch of the NWP, who helped raise funds to bring an organizer to
          the state of Iowa.</p>
        <p>The second is a 1917 photograph of a <a href="http://hdl.loc.gov/loc.mss/mnwp.276023"
            target="_blank">suffrage picket line</a>. The first woman in line is from Eagle Grove, Iowa. </p>

        <p><strong>Radcliffe Institute for Advanced Study. Harvard University. Schlesinger Library.<br>
            <a href="http://www.radcliffe.edu/schlesinger_library.aspx" target="_blank">
              http://www.radcliffe.edu/schlesinger_library.aspx</a></strong></p>
        <p>The Schlesinger Library dates from August 26, 1943, when Radcliffe College received a collection of books,
          papers, and memorabilia on women reformers from alumna and suffragist Maud Wood Park. Her “Woman’s Rights
          Collection” became the nucleus of a research library called the Women’s Archives, which the college
          gradually augmented during the 1940s and 1950s. When the women’s movement surged in the 1960s and 1970s, the
          library’s collections grew rapidly as feminist activists recognized the importance of women’s history and
          created their own documents and publications. The collections listed below have a connection to the state of
          Iowa. Most of the documents themselves are not online, but links to the finding aids are included below.</p>

        <p></p>
        <ul>
          <li><a href="http://oasis.lib.harvard.edu/oasis/deliver/deepLink?_collection=oasis&amp;uniqueId=sch00513"
              target="_blank">Coggeshall, Mary Jane Whitely, 1836-1911</a><br> Mary Jane Whitely Coggeshall was born
            in 1836. Known as “the Mother of Woman Suffrage in Iowa,” Coggeshall was a founding member of the Polk
            County Iowa Woman Suffrage Society in 1870. She held numerous positions in the Iowa Equal Suffrage
            Association and served three terms as president. She was elected an auditor for the National American
            Woman Suffrage Association in 1902 and 1904, attended several NAWSA conventions, and was a speaker at
            those held in Washington, D.C. and Chicago. She also served as editor of <em>The Woman’s Standard</em> for
            its first two years of publication (1886-1888) and its last (1911). <p></p>
            <p>This collection, consisting almost entirely of her speeches and writings, is arranged in three
              sections: Iowa, NAWSA, and biographical sketches. The bulk of the collection is made up of Iowa
              material: speeches, responses by Iowa editors to the question of publishing suffrage articles, a history
              of the Polk County Woman Suffrage Society, a report on the 1904 state convention, and several letters to
              the editor, as well as clippings about the 1902 state convention, a suffrage poster, and memorabilia.
              The NAWSA section consists of speeches at and reports of national conventions. </p>
          </li>
        </ul>
