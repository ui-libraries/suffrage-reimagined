            Chicago, in Iowa, and later in California. The collection includes clippings, programs, flyers, brochures,
            reports, correspondence, photographs, minutes, and speeches documenting the suffrage movement,
            particularly in Illinois. Correspondents include well-known women involved in the suffrage and temperance
            movements. Clippings from the “Woman’s Kingdom” were pasted into scrapbooks and provide information about
            a variety of women’s activities, particularly in the Midwest.</li>
