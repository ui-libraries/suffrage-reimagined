            <ul>
              <li><strong><em>The Trident</em> newsletter, ed. by Ella Bushnell Hamlin. 1904-1906. 2 folio
                  cases.</strong>
                <p></p>

                <p>Bushnell Hamlin founded <em>The Trident</em> newsletter in 1904 to cover news, society and women’s
                  issues
                  in the Tri City Area, which included Davenport, Moline and Rock Island.</p>
                <p> Bushnell Hamlin was a suffragist and her publications often covered national and international
                  suffrage
                  news. She advocated for women’s involvement in clubs, and <em>The Trident</em> is full of clubwomen
                  news.
                  She refused to pay a license tax in 1909 and urged the mayor to throw her in jail. In 1910, she
                  campaigned
                  for the position of Davenport school director, and <em> The Trident </em>ceased publication. Although
                  a
                  fascinating source of society and business life, the newsletters provide little local information on
                  suffrage. They have been scanned and will eventually be placed in the <a href="http://ir.uiowa.edu/"
                    target="_blank">Iowa Institutional Repository</a>.</p>
                <p>The <a href="http://www.putnam.org/" target="_blank">Putnam Museum</a> in Davenport has additional
                  clippings and publications by the Trident Publishing Company for 1904 and 1911.</p>
              </li>
            </ul>
            <p></p>
