                    target="_blank">University of Iowa literary and debate societies and clubs (1890s - )</a></strong>
                <p></p>

                <p>UI Special Collections has records from many literary and debate societies and university clubs. Some
                  are
                  specifically related to women, others are men’s clubs. In the 1890s and early 1900s many of the men’s
                  clubs
                  at the university had female counterparts with which they would socialize and plan events.
                  Two men’s groups whose records briefly record women’s suffrage debates are Zetagathian and
                  Philomathean.
                  Most of the women’s club records make no mention of suffrage or women’s rights.</p>
              </li>
            </ul>
