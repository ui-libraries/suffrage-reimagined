        <p><strong>Beran, Janice A. <em>The League of Women Voters of Iowa: A Voice for Citizens, a Force for
              Change.</em> Des Moines, Iowa: League of Women Voters of Iowa, 1996.</strong></p>
        <p>Created as a way to celebrate the League of Women Voters of Iowa’s 75th anniversary in 1995, this booklet
          includes documentation of League members’ own stories about their involvement in League activities, studies,
          and action programs. Brief accounts of its founding and highlights of events throughout its history showcase
          the impact that the League of Women Voters had on individuals, their community, and local and state
          governments.</p>

        <p><strong>Bloomer, Amelia. <em>Iowa</em>. Vol. 3, chap. XLV in <em>History of Woman Suffrage 1876-1885</em>,
            ed. Elizabeth Cady Stanton, Susan B. Anthony and Matilda Joslyn Gage, 612-637. Rochester, New York:
            Charles Mann Printing Co., 1886</strong></p>
        <p>Part of a massive history of women’s suffrage compiled by the leaders of the National American Woman
          Suffrage Association, the Iowa chapter portrays the state as a land of honeysuckle and wild roses with
          “moral, virtuous, and intelligent people.”</p>
        <p> Written by well-known suffragist Amelia Bloomer, this chapter was heavily edited by Elizabeth Cady Stanton
          and does not mention the free-love debate of the 1870s or suffragist Annie Savery. (See Louise Noun’s <em><a
              href="http://digital.lib.uiowa.edu/u?/suffrage,2524" target="_blank">Annie Savery and the Campaign for
              Women’s Rights in Iowa</a></em>.) Bloomer does, however, chronicle nineteenth century struggles of Iowa
          suffragists and conveys the hopeful mood of the mid-1880s. The full text of this work, including the Iowa
          chapter, is available on <a
            href="http://books.google.com/books?id=CPYLAQAAIAAJ&amp;dq=history%20of%20woman%20suffrage%20volume&amp;pg=PR1#v=onepage&amp;q=history%20of%20woman%20suffrage%20volume&amp;f=false"
            target="_blank">Google Books</a>. </p>

        <p><strong>Bloomer, D.C., ed. <em>Life and Writings of Amelia Bloomer</em>. New York: Schocken Books,
            1975.</strong></p>
        <p>Dexter C. Bloomer gives a glowing account of his wife’s suffrage and temperance work, including
          descriptions of her work in Council Bluffs starting in 1855. Bloomer’s account illustrates how difficult
          suffrage and temperance work were in an Iowa frontier town.</p>

        <p><strong>Bordin, Ruth. <em>Woman and Temperance: The Quest for Power and Liberty, 1873-1900</em>. ed. Allen
            F. Davis. Philadelphia: Temple University Press, 1981.</strong></p>

        <p>Bordin provides analysis of the Woman’s Christian Temperance Union (WCTU) as an organization and a social
          movement. She devotes considerable space to suffrage activities of the WCTU, including a brief explanation
          of campaigns in Iowa in the early 1880s. The inter-organizational arguments over partisanship and the
          subsequent split with the Iowa WCTU delegation are also discussed. </p>
        <p>Bordin claims the WCTU provided women with an acceptable avenue to advocate for suffrage within existing
          religious and family structures. Bordin has also written a biography of Frances Willard, national president
          of the WCTU during the organization's early suffrage efforts.</p>


        <p><strong>Buhle, Mari Jo, and Paul Buhle, eds. <em>The Concise History of Woman Suffrage: Selections from
              History of Woman Suffrage edited by Elizabeth Cady Stanton, Susan B. Anthony, Matilda Joslyn Gage and
              The National American Woman Suffrage Association</em>. Urbana, Illinois: University of Illinois Press,
            2005.</strong></p>
        <p>The Buhles critically analyze the <em>History of Woman Suffrage</em> as a partisan and defensive view of
          the National American Woman Suffrage Association (NAWSA), only one organization in the national struggle for
          suffrage. Pulling excerpts from Stanton, Anthony, and Gage’s original work, they contextualize certain
          events and provide a more nuanced view of the movement. Iowa is specifically mentioned in regards to
          Elizabeth Cady Stanton’s 1871 lecture tour, where she spoke out about birth control and the state’s support
          for the 1877 Colorado suffrage campaign. </p>



        <p><strong>Catt, Carrie Chapman, and Shuler, Nettie Rogers. <em>Woman Suffrage and Politics: The Inner Story
              of the Suffrage Movement</em>. New York: Charles Scribner's Sons, 1923.</strong></p>
        <p>Written shortly after women won the vote, Catt and Schuler look back at the work of the National American
          Woman Suffrage Association (NAWSA). They provide insight into lessons learned from legislative failures in
          Iowa and how women’s suffrage crossed the “devious trail” of politics. Although written as a history, it is
          more a first-hand account of the struggles and triumphs Catt saw through her involvement with NAWSA.</p>

        <p><strong>Coon, Anne C., ed. <em>Hear Me Patiently: The Reform Speeches of Amelia Jenks Bloomer</em>.
            Westport, Conn: Greenwood Press, 1994.</strong></p>
        <p>Amelia Jenks Bloomer started as a temperance advocate but soon developed strong opinions about women’s
          rights through her work outside of the traditional “woman’s sphere.” She became a popular lecturer in the
          early to mid-1850s, speaking about social, moral, and legal themes. She believed that “for woman to vote. .
          .is to elevate her in the scale of humanity,” and stressed this throughout her speeches.</p>
        <p>Speeches in this book include “Equality in Rights and Privileges,” which argues for <em>human</em> rights
          (December 7, 1855 Council Bluffs Methodist church), “Alas! Poor Adam,” which responds to Biblical arguments
          against suffrage (June 1870, Des Moines Baptist church,) and “A Principle of All Free Governments,” in which
          Bloomer uses the title phrase “hear me patiently” to refute arguments against enfranchisement.</p>
        <p>The introduction includes commentary and photos of the famous “bloomer costume” and a reference to
          Bloomer’s feelings of resentment toward Elizabeth Cady Stanton and Susan B. Anthony after her contributions
          to the book <em>History of Woman Suffrage</em> were heavily edited.</p>

        <p><strong>DuBois, Ellen Carol. <em>Feminism and Suffrage: The Emergence of an Independent Women's Movement in
              America 1848-1869</em>. Ithaca: Cornell University Press, 1978.</strong></p>
        <p>DuBois analyzes the women's suffrage movement through the context of feminism and claims suffrage was not
          an isolated institutional reform, but a true social movement. Early Iowa struggles are mentioned several
          times.</p>

        <p><strong>Egge, Sara Anne. “The Grassroots Diffusion of the Woman Suffrage Movement in Iowa: the IESA, Rural
            Women, and the Right to Vote.” Ames, Iowa: Masters thesis, Iowa State University, 2009.</strong></p>
        <p>This thesis examines how rural women thought of, talked about, and organized around women’s suffrage. By
          looking at a segment of the population often ignored by suffrage histories, Egge uncovers some unique
          challenges faced by suffrage activists in farming communities, but also shows that rural Iowa women were
          aware of the suffrage debate. She includes a discussion of several automobile tours used to promote suffrage
          in Iowa in the early 1910s.</p>

        <p><strong>Evans, Sara M. <em>Born for Liberty: A History of Women in America</em>. New York: Simon &amp;
            Schuster, 1997.</strong></p>
        <p>Evans places the suffrage movement in historical context by providing a broad look at how social, political
          and economic factors affected women and women’s movements throughout American history. She also discusses
          how the national suffrage movement changed over time and how that may have influenced state organizations.
          The town that hosted a suffrage parade in 1908, which Evans calls Boomer, Iowa, refers to Boone, Iowa.</p>

        <p><strong>Flexner, Eleanor, and Fitzpatrick, Ellen. <em>Century of Struggle: The Woman’s Rights Movement in
              the United States</em>. Cambridge, Mass.: The Belknap Press of Harvard University, 1996.</strong></p>
        <p>Considered one of the seminal works on women’s suffrage in the United States, this book discusses social
          and political factors that influenced the suffrage movement. Iowa’s role in the national campaign strategy
          and Carrie Chapman Catt’s leadership are discussed in Chapters 21 and 22. Flexner and Fitzpatrick also
          examine the opposition of Midwest brewing companies and associations to suffrage and the impact this
          opposition had on state campaigns. </p>

        <p><strong>Gallaher, Ruth. <em>Legal and Political Status of Women in Iowa</em>. Iowa City, Iowa: State
            Historical Society of Iowa, 1918.</strong></p>
        <p>Gallaher traces suffrage trends in the United States from the colonial era through the early twentieth
          century before focusing on the history of suffrage in Iowa.</p>
        <p>Although published before a final suffrage victory, this book is thorough in its coverage of legislative
          efforts from 1843 through 1918, as well as the efforts of Iowa players Amelia Bloomer, Carrie Chapman Catt,
          and Helen M. Gougar, the first woman to address the House of Representatives in the new state capitol
          building in 1888. </p>
        <p>Gallaher mentions clubs such as the Northern Woman Suffrage Association in Dubuque and the Men’s League for
          Woman Suffrage. Gallaher also researched gubernatorial support or opposition during this time frame -
          including Governor Larrabee’s experimental support of school and municipal suffrage. When Gallaher wrote,
          property owning Iowa women had been granted the right to vote on municipal and school elections regarding
          levy and tax issues, as well as drainage district administrative concerns.</p>

        <p><strong>Giele, Janet Zollinger. <em>Two Paths to Women's Equality: Temperance, Suffrage and the Origins of
              Modern Feminism</em>. New York: Twayne Publishers, 1995.</strong></p>
        <p>Giele examines what she sees as two separate and distinct feminist movements: temperance and suffrage. She
          discusses the role of the Woman’s Christian Temperance Union (WCTU) and the leading suffragists through the
          lens of the 1970s women’s movement. Although Giele does not specifically look at Iowa, women with Iowa ties
          such as Amelia Bloomer and Carrie Chapman Catt are mentioned.</p>

        <p><strong>Horack, Frank E. <em>Equal Suffrage in Iowa</em>. Vol. II, in <em>Applied History</em>, ed.
            Benjamin F. Shambaugh. Iowa City, Iowa: State Historical Society of Iowa, 1914.</strong></p>
        <p>Horack presents arguments for and against suffrage in the years leading up to the 1916 referendum. Part of
          a larger examination of Iowa government and social legislation, this chapter provides insight into the
          rhetoric used by suffragists and anti-suffragists in Iowa at this time. This section also includes a
          <em>Minority Report on Suffrage</em> by Thomas B. Reed arguing for women’s right to vote. The full text of
          Horack’s essay is available on <a
            href="http://books.google.com/books?id=gY4OAAAAYAAJ&amp;dq=equal%20suffrage%20in%20iowa%20frank%20horack&amp;pg=PA275#v=onepage&amp;q=equal%20suffrage%20in%20iowa%20frank%20horack&amp;f=false"
            target="_blank">Google Books.</a></p>

        <p><strong>Hudson, David; Bergman, Marvin; and Horton, Loren. <em>Biographical Dictionary of Iowa</em>. Iowa
            City: University of Iowa Press, 2010.</strong></p>
        <p>This biographical dictionary highlights important figures in Iowa history with brief biographies compiled
          from primary and secondary sources specific to the state. Suffragists profiled include: Mary Newbury Adams,
          Martha C. Callanan, Mary Jane Coggeshall, Flora Dunlap, and Anna Bell Lawther.</p>
        <p>The Iowa Digital Library has scanned the entire <a href="http://digital.lib.uiowa.edu/uipress/bdi/"
            target="_blank">Biographical Dictionary of Iowa</a> for online use. Users can search the book and browse
          by topic, including women's rights.</p>



        <p><strong>Kraditor, Aileen S. <em>The Ideas of the Woman Suffrage Movement 1890-1920</em>. Garden City:
            Anchor Books, Doubleday and Company, 1971.</strong></p>
        <p>Kraditor looks at the ideas behind the suffrage movement and debunks the myth that suffragists had an
          official ideology or even a unified vision. She examines subjects such as religion, politics, and social
          reform and explains how ideas within those spheres changed with time, as did the suffrage movement. She also
          devotes considerable space to the suffrage movement in the South and racism within various suffrage
          organizations.</p>
        <p> Mary Jane Coggeshall of Iowa is briefly mentioned, as is Carrie Chapman Catt. This work provides a good
          background on the arguments for and against suffrage and the context in which these were made.</p>

        <p><strong>Marshall, Susan E. <em>Splintered Sisterhood: Gender and Class in the Campaign Against Woman
              Suffrage</em>. Madison: University of Wisconsin Press, 1997.</strong></p>
        <p>Marshall takes a look at motivations behind the anti-suffrage movement and concludes that many “antis” were
          women who wanted to protect the status quo. She mentions several powerful Iowans opposed to suffrage,
          including members of the Iowa Association of Men Opposed to Woman Suffrage, which was formed to defeat the
          1916 referendum. She also examines the role of immigrants and brewing interests in opposing suffrage in the
          Midwest.</p>


        <p><strong>National American Woman Suffrage Association. <em>Victory: How Women Won It--A Centennial Symposium
              1840-1940</em>. New York: H.W. Wilson Company, 1940.</strong></p>
        <p>Carrie Chapman Catt wrote the introduction and one of the chapters for this book. Written as
          totalitarianism and communism were spreading in Europe and Asia, it paints a rosy picture of American
          women’s fight for democracy. It is also very NAWSA-centric, similar to <em>History of Woman Suffrage</em>
          and <em>Woman Suffrage and Politics</em>. There are a few timelines of state milestones in the appendices.
        </p>


        <p><strong>Noun, Louise R. with Rachel Bohlmann. <em>Leader and Pariah: Annie Savery and the Campaign for
              Women's Rights in Iowa</em>. Iowa City: Iowa Women's Archives, 2002.</strong></p>
        <p>Annie Savery was an active suffragist in the early days of Iowa’s suffrage movement, but she was
          blacklisted after supporting a woman’s right to hold opinions outside of the social mores of the day. Her
          part in Iowa history was edited out of Elizabeth Cady Stanton and Susan B. Anthony’s <em>History of Woman
            Suffrage</em>. Noun reprints speeches and letters written by Savery and provides insight into internecine
          warfare of the Iowa suffragists. This book is available in its entirety in the <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,2524" target="_blank">Women’s Suffrage in Iowa Digital
            Collection</a>.</p>


        <p><strong>Noun, Louise R. <em>Strong-Minded Women: The Emergence of the Woman-Suffrage Movement in Iowa</em>.
            Ames, Iowa: Iowa State University Press, 1969.</strong></p>
        <p>Noun authored the seminal works on women's suffrage in Iowa. This book is cited in many national accounts
          of the suffrage movement. It focuses on the nineteenth century struggles Iowa women faced in obtaining the
          right to vote. Rich in details, this book describes the major events and players of the Iowa suffrage
          movement. Noun was thorough in her research and her bibliography is a valuable resource which lists primary
          source materials across the country and local Iowa newspapers that covered suffrage. This book is available
          in its entirety in the <a href="http://digital.lib.uiowa.edu/u?/suffrage,2308" target="_blank">Women’s
            Suffrage in Iowa Digital Collection</a>. (Noun’s research notes for this book are located in <a
            href="http://www.lib.grinnell.edu/collections/specialcollections/Manuscripts/findingaids/Nounmss.html"
            target="_blank">Grinnell College Special Collections</a>).


        </p>
        <p><strong>Schwalm, Leslie. <em>Emancipation’s Diaspora: Race and Reconstruction in the Upper Midwest.</em>
            Chapel Hill: University of North Carolina Press, 2009.</strong></p>
        <p>Schwalm investigates the ways in which the upper Midwest’s black and white residents became engaged in a
          process of redefining the meanings and implications of freedom and citizenship. Chapter 6 discusses how,
          even as the most economically disenfranchised of the Midwest’s black residents, African American women
          claimed the prerogative both to endorse an idealized black manhood and to challenge the extent of male
          privilege in their communities. Specifically, issues of personal and familial gendered viewpoints are
          touched on in relation to the law and practice of discrimination, as well as the resulting increased
          activism for some black mothers living in the Midwest.</p>

        <p><strong>Scott, Anne Firor, and Andrew MacKay Scott. <em>One Half the People: The Fight for Woman
              Suffrage</em>. Urbana: University of Illinois Press, 1982.</strong></p>
        <p>The Scotts provide a basic, brief introduction to the suffrage movement, drawing on the work of Ellen
          DuBois among others. Further, they include primary documents highlighting moments of the movement. One of
          these is Maud Wood Park’s recollection of the unveiling of Carrie Chapman Catt’s “winning plan” in which the
          National American Woman Suffrage Association (NAWSA) attacked the suffrage question from the federal and
          state levels. </p>

        <p><strong>Silag, Bill, ed. <em>Outside In: African-American History in Iowa, 1838-2000</em>. Des Moines:
            State Historical Society of Iowa, 2001.</strong></p>
        <p>This overview of African-American history in Iowa includes a chapter on women by Kathryn M. Neal, that
          highlights African-American women “pioneers” who broke ground professionally or women considered to be
          “builders of their communities” by serving in clubs and organizations.</p>
        <p> The sections about the women of Buxton and Vivian Smith of the Waterloo Suffragette Council illuminate
          African-American women’s participation in the state’s suffrage movement.

        </p>
        <p><strong>Tucker, Cynthia Grant. <em>Prophetic Sisterhood: Liberal Women Ministers of the Frontier,
              1880-1930</em>. Boston: Beacon Press, 1990.</strong></p>
        <p>Tucker focuses on Unitarian and Universalist female ministers in the Midwest. A significant number of these
          women lived and worked in Iowa and many of them were also active in the suffrage movement including: Mary
          Safford, Eleanor Gordon, and Mary Collson. TUcker describes the social reform and religious activities of
          these suffragists and how these women interacted with other Iowa suffragists and the national organizations.
        </p>

        <p><strong><em>Women's Suffrage in Iowa: 90 Years After the “Winning Plan”</em>. Des Moines: Blanden Art
            Museum, 2009.</strong></p>
        <p>Published as a catalog to the Blanden Art Museum’s exhibition of suffrage paintings, photographs, and
          artifacts, this brief, visual history of the Iowa suffrage movement provides a good introduction for
          readers. The catalog includes an essay about Carrie Chapman Catt as well as an overview of the suffrage
          movement in Iowa. This work is available in its entirety in the <a
            href="http://digital.lib.uiowa.edu/u?/suffrage,2716" target="_blank">Women’s Suffrage in Iowa Digital
            Collection</a>.</p>
